package com.nls.masternaut;

public class LocationCategory {
    private String id;
    private String name;
    private String icon;
    private Integer locationsCount;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getIcon() {
        return icon;
    }

    public Integer getLocationsCount() {
        return locationsCount;
    }
}
